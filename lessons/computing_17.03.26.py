# COMPUTING — Arithmetic & Operators
#
# You've used + and * already. Python has a few others worth knowing:
#
#   +   addition
#   -   subtraction
#   *   multiplication
#   /   division (always returns a float)
#   //  floor division (rounds down to nearest whole number)
#   %   modulo (returns the remainder after division)
#   **  exponentiation (power of)
#
# The interesting ones are // and %.
# What do you think 10 // 3 returns? And 10 % 3?
# Have a guess as a comment, then run it to check.

print(f"10 divided by 3 is {10 //3} with a remainder of {10 % 3}")

print(f"the 67th hour is actually the {67%24}th hour.")
